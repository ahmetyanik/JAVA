import java.util.Scanner;

// break ve continue anahtar kelimeleri
//break döngüyü sona erdirir.
//continue altindaki islemleri yapmadan döngünün en basina gider

public class Continue {
	
	public static void main(String[] args) {
	
		int i=0;
		
		while(i<10) {
			// 4 ve 7'yi bastirmayalim
			
			if (i==4 || i==7) {
				i++;
				continue;
			}
			
			System.out.println("i: "+ i);
			i++;
			
			
		
		}}}
		
		
		
		
		
	

	