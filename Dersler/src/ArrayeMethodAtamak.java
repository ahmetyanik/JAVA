
public class ArrayeMethodAtamak {

public static void arrayi_bastir(int[] array) {
		
		for(int i =0;i<array.length;i++) {
			
			System.out.println("Element"+(i+1)+":"+array[i]);
			
		}	}
	
	
	public static void main(String[] args) {
		
		int [] a = {10,20,30,40,50};
		
		arrayi_bastir(a);		

	}

}
