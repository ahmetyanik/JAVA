package day06ifstatetement;

public class IfStatement01 {

	public static void main(String[] args) {
		
		
		// bir int variable olusturun ona bir deger atayin
		// eger atadiginiz deger positiv ise pozitif yazdirin
		
		
		int x = 7;
		
		if(x<0) {
			
			
			System.out.println("Sayi: "+x+" negatiftir.");
		}
		
		if(x>0)
			System.out.println("Sayi: "+x+" pozitiftir.");
		
		

	}

}
