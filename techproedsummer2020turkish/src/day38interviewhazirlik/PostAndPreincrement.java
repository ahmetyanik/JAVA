package day38interviewhazirlik;

public class PostAndPreincrement {
	
	public static void main(String[] args) {
		
		
		int a = 12;
		a = a++;
		
		System.out.println(a);//12
		
		
		//post increment
		int b = 22;
		b ++;
		
		System.out.println(b);//23
		
		
		//preincrement
		int c = 32;
		c = ++c;
		System.out.println(c); //33
		
	}
	
	

}
